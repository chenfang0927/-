"""
    函数内存分布
    1. 传入可变对象
    2. 修改可变对象
    3. 函数外可以得到结果(不用return)
    练习:exercise10
"""

# 将函数的代码存储到代码区中
def func01(p1, p2):
    p1[0] = 100
    p2 = 200

list01 = [10]
list02 = [20]
# 调用函数在内存中开辟一块空间(栈帧),
# 用于存储在函数中创建的变量
func01(list01, list02)
# 函数执行后栈帧释放
print(list01[0])  # 100
print(list02[0])  # ?
